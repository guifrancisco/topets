package com.example.topets.enums;

public enum DietType {
    /**
     * A diet consisting of natural and minimally processed ingredients.
     */
    NATURAL,
    /**
     * A diet formulated for specific health conditions or dietary restrictions, such as food for pets with allergies or sensitivities.
     */
    SPECIALIZED,
    /**
     * Commercially prepared pet food, which can include dry kibble, wet food, and treats.
     */
    COMMERCIAL,
    /**
     * A diet prepared at home, where the pet owner selects and cooks the ingredients.
     */
    HOMEMADE
}
